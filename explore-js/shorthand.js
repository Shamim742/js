// variable declaration
let tomatoPrice = 25;
// variable value update 
tomatoPrice = 30;
console.log(tomatoPrice)

// add something to previous value
let price = 45;
let price1 = 30;
// set a new value 
// add something to existing value 
price = price + 5;
price1 = price1 + 70;
console.log(price + price1)
// addition to the same value shorthand
// += means add value to the existing variable
price += 50;
price1 += 70;
console.log(price, price1)

// -= means deducting some values from the current value

price -= 5;
console.log(price)
// multiply num
price *= 4;
console.log(price)

// divided num
price /= 5;
console.log(price)

// increase num by one 
let x = 4;
x++;
console.log(x)
// decrease num by one 
let y = 10;
y--;
console.log(y)